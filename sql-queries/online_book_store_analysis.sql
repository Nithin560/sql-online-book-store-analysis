-- FIND THE TOP 3 CUSTOMERS IN EACH COUNTRY BASED ON TOTAL SPENDING
SELECT *
FROM
(
    SELECT c.country,
           c.customer_id,
           c.name,
           ROUND(SUM(o.total_amount),2) AS total_spending,
           RANK() OVER(
               PARTITION BY c.country
               ORDER BY SUM(o.total_amount) DESC
           ) AS customer_rank
    FROM customers c
    JOIN orders o
        ON c.customer_id = o.customer_id
    GROUP BY c.country, c.customer_id, c.name
) t
WHERE customer_rank <= 3;

