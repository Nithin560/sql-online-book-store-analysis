-- FIND ALL BOOKS WHOSE TOTAL SALES QUANTITY IS 
-- GREATER THAN THE AVERAGE SALES QUANTITY OF ALL BOOKS
SELECT b.book_id, b.author, 
SUM(o.quantity) AS total_sales_quantity
FROM books as b
JOIN orders as o
ON o.book_id = b.book_id
GROUP BY b.book_id, b.author
HAVING SUM(o.quantity) > 
	(SELECT AVG(o.quantity) as AVG_SALES_QUANTITY FROM orders as o);
    
-- FIND ALL CUSTOMERS WHOSE TOTAL SPENDING IS 
-- GREATER THAN THE AVERAGE SPENDING OF ALL CUSTOMERS
SELECT c.customer_id, c.name, ROUND(SUM(o.total_amount),2) as TOTAL_SPENDING
FROM customers as c
LEFT JOIN orders as o
	ON c.customer_id = o.customer_id
GROUP BY c.customer_id, c.name
HAVING SUM(o.Total_Amount) > 
	(SELECT AVG(customer_spending) FROM 
		(SELECT customer_id, SUM(total_amount) AS customer_spending
		 FROM orders GROUP BY customer_id) t
     );

